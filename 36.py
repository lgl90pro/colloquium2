'''36. Знайти суму додатніх елементів лінійного масиву цілих чисел.
Розмірність масиву - 10. Заповнення масиву здійснити з клавіатури.
Виконав: Лещенко В. О.'''

import numpy as np

A = np.zeros(10, dtype=int)
for i in range(10):
    A[i] = int(input(f'A[{i + 1}] = '))

sum = 0
for i in A:
    if (i > 0): # якщо елемент додатній - сумуємо його
        sum += i

print(f'Сума додатніх елементів: {sum}.') # вивід