'''32. Змінній t привласнити значення істина, якщо в одновимірному масиві є
хоча б одне від’ємне і парне число.
Виконав: Лещенко В. О.'''

import numpy as np

A = np.random.randint(-10, 30, 10)
print(f'Вихідний масив: {A}.\n')

for i in A:
    if (i < 0 and i % 2 == 0): # перевіряємо умову
        t = True # якщо виконується, т = істинна
        break # вихід з циклу
    else:
        t = False # інакше - хиба

print(t) # вивід