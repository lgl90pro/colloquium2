'''7. Створіть масив А [1..12] за допомогою генератора випадкових чисел з
елементами від -20 до 10 і виведіть його на екран. Замініть всі від’ємні елементи
масиву числом 0.
Виконав: Лещенко В. О.'''

import numpy as np

A = np.random.randint(-20, 10, 12)
print(f'Вихідний масив: {A}.\n')

for i in range(len(A)):
    if (A[i] < 0): # якщо елемент від'ємний,
        A[i] = 0 # то замінюємо його на нуль

print(f'Масив після перетворення: {A}.') # вивід