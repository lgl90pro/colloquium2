'''14. Сформуйте лінійний масив дійсних чисел, елементи якого є відстанями,
пройденими тілом при вільному падінні на землю за 1, 2, ..., 10 с.
Виконав: Лещенко В. О.'''

import numpy as np

A = np.zeros(10)
t = 1 # початкове значення в секундах
for i in range(10):
    A[i] = 9.8 * t ** 2 / 2 # відстань при вільному падінні знаходимо за формулою і додаємо на відповідне місце в масиві
    t += 1 # додаємо +1 секунду кожну ітерацію

print(A) # вивід
