'''5. Створіть масив А [1..7] за допомогою генератора випадкових чисел і
виведіть його на екран. Збільште всі його елементи в 2 рази.
Виконав: Лещенко В. О.'''

import numpy as np

A = np.random.randint(-10, 10, 7) # за допомогою random.randint створюємо рандомний масив із 7 елементів від -10 до 10
print(f'Вихідний масив: {A}.\n') # виводимо масив до перетворення
print(f'Подвоєний масив: {A*2}.') # виводимо, подвоюючи кожний елемент за допомогою *