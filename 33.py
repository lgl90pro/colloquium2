'''33. Заданий масив А (1:20). Знайти добуток всіх його ненульових елементів.
Виконав: Лещенко В. О.'''

import numpy as np

A = np.random.randint(0, 5, 20)
print(f'Вихідний масив: {A}.\n')

prod = 1
for i in A:
    if (i != 0): # якщо елемент ненульовий, перемножуємо
        prod *= i

print(f'Добуток ненульових елементів: {prod}.')